package kasei.javase.se.syntax.operator;

//没有特别注释的的操作符，都只能操作基本类型
//加：+               (String 类支持 “+”（注意：String类使用+号时会自动调用对象的toString()方法)
//减：-
//乘：*
//除：/
//取余：%             (只能用于整数)
//与：&&
//或：||
//非：!
//按位与：&
//按位或：|
//按位取反：~
//按位异或：^
//等于：==            (能操作所有的对象)
//不等于：!=          (能操作所有的对象)
//赋值：=             (能操作所有的对象)
//低位补0左移：<<
//高位补符号位右移：>>
//高位补0右移：>>>
//条件运算符：a>b?a:b